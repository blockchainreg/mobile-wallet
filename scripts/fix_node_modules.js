const fs = require('fs');
const folder = './';
const replaceWhat = 'void(';
const replaceTo = 'void(';

function replaceFileSync(path) {
  var content = fs.readFileSync(path, 'utf8');
  var newContent = content.split(replaceWhat).join(replaceTo);
  if (newContent === content) return;
  fs.writeFileSync(path, newContent, 'utf8');
  console.log('Replaced', path);
}

function replaceFolerSync(path) {
  // console.log(path);
  fs.readdirSync(path).forEach((name) => {
    if (name.startsWith('.')) return;
    var newPath = path + '/' + name;
    if (fs.statSync(newPath).isDirectory()) return replaceFolerSync(newPath);
    replaceFileSync(newPath);
  });
}
replaceFolerSync(folder);
